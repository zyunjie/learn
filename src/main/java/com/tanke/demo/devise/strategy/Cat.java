public class Cat {

    int weight;
    int height;

    public Cat(int weight,int height){
        this.weight=weight;
        this.height=height;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }


}
